#string slicing
#prefix[start_index:end_index:step]
s="python"
print(s[0:5:3])
print(s[0:5:2])
#print(s[0:5:0])
print(s[0:10:2])
print(s[0:6:1])
print(s[4:0:-1])
print(s[4:0:-2])
print(s[4:0:1])
print(s[0:4:-1])
print(s[-6:-1:1])
print(s[-2:-6:-1])
print(s[0:-1:1])
print(s[-2:1:-1])
print(s[-2:1:])
print(s[0:-1:])
print(s[0:-1])
print(s[:2:-1])
print(s[:2:1])
print(s[:2])
print(s[2::-1]) #end-->(-n-1)
print(s[2::1])  #end-->n
print(s[::-1])







