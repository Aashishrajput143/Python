#Assignment Operators(Binary)

#Simple Assignment
x=10
print(x)

y=x
print(y)

z=x+y+10
print(z)

#Multiple Assignment
a,b=10,20
print(a,b)

#Chained Assignment
a=b=c=10
print(a,b,c)

#Compound(short hand) Assignment
x=5
print(x)

x=x+2
print(x)

x+=2  #x=x+2
print(x)

x*=3 #x=x*3
print(x)

